    private void removerUsuarioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerUsuarioButtonActionPerformed

    String nome = JOptionPane.showInputDialog(this, "Digite o nome do usuario que deseja remover:");

    if (nome != null && !nome.trim().isEmpty()) {
        try {
            boolean removido = UsuarioDAO.removerUsuario(nome);
            if (removido) {
                JOptionPane.showMessageDialog(this, "usuario " +nome+" removido  com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "usuario não encontrado.");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao remover usuario: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "usuario "+nome+" inválido.");
    }


    }//GEN-LAST:event_removerUsuarioButtonActionPerformed

