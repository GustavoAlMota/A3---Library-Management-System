    private void alterarUsuarioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarUsuarioButtonActionPerformed
        String nome = nomeTextField.getText();
        String senha = senhaTextField.getText();
        String tipo = (String) tipoUsuarioComboBox.getSelectedItem();

        Usuario u = new Usuario();
        u.setNome(nome);
        u.setSenha(senha);
        u.setTipo(tipo);

        if (tipo.equals("comum")) {
            String preferenciaLivro1 = (String) preferenciaLivro1ComboBox.getSelectedItem();
            String preferenciaLivro2 = (String) preferenciaLivro2ComboBox.getSelectedItem();
            u.setPreferenciaLivro1(preferenciaLivro1);
            u.setPreferenciaLivro2(preferenciaLivro2);
        }

        try {
            UsuarioDAO.alterarUsuario(u);
            JOptionPane.showMessageDialog(this, "Usuário "  + nomeTextField.getText() +" alterado com sucesso!");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao alterar o usuário: " + e.getMessage());
        }


    }//GEN-LAST:event_alterarUsuarioButtonActionPerformed

    private void cadastrarUsuarioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarUsuarioButtonActionPerformed
        String nome = nomeTextField.getText();
        String senha = senhaTextField.getText();
        String tipo = tipoUsuarioComboBox.getItemAt(tipoUsuarioComboBox.getSelectedIndex());
        String preferencia1 = (String) preferenciaLivro1ComboBox.getSelectedItem();
        String preferencia2 = (String) preferenciaLivro2ComboBox.getSelectedItem();

        Usuario usuario = new Usuario(nome, senha, tipo, preferencia1, preferencia2);
        try {
            UsuarioDAO.cadastraUsuario(usuario);
            JOptionPane.showMessageDialog(null, "Usuario: " + nomeTextField.getText() + " cadastrado com sucesso");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Usuario: " + nomeTextField.getText() + " ja está cadastrado", "ups ", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_cadastrarUsuarioButtonActionPerformed

